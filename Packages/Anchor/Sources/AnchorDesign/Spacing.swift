import SwiftUI

/// Spacing scale. Generous whitespace and low density are product
/// requirements, not taste — prefer the larger step when unsure.
public enum Spacing {
    public static let xs: CGFloat = 4
    public static let s: CGFloat = 8
    public static let m: CGFloat = 16
    public static let l: CGFloat = 24
    public static let xl: CGFloat = 40
}
